// Function to display color lists in columns
function displayColorLists() {
    const colorContainer = document.getElementById("color-container");

    colorLists.forEach((colorList, index) => {
        const column = document.createElement("div");
        column.classList.add("color-column");

        colorList.forEach((color, colorIndex) => {
            const colorDiv = document.createElement("div");
            colorDiv.classList.add("color-item");
            colorDiv.style.backgroundColor = color;
            colorDiv.textContent = `List ${index + 1}, ${color}`;
            column.appendChild(colorDiv);
        });

        colorContainer.appendChild(column);
    });
}

// Call the function to display color lists when the page loads
window.addEventListener("load", displayColorLists);
